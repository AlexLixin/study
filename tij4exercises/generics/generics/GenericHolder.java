package generics;

public class GenericHolder<T> {

    private T t;

    public void set(T t) {
        this.t = t;
    }

    public T get() {
        return t;
    }

    public static void main(String[] args) {
        GenericHolder<String> gh = new GenericHolder<>();
        gh.set("Item");
        String s = gh.get();
        System.out.println(s);
    }

}
